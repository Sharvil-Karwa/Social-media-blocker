switch (window.location.hostname) {
  case "www.youtube.com":
    document.body.innerHTML = "Studying>Youtube";
    break;
  case "www.facebook.com":
    document.body.innerHTML = "Studying>Facebook";
    break;
  case "www.netflix.com":
    document.body.innerHTML = "Studying>Netflix";
    break;
  case "www.instagram.com":
    document.body.innerHTML = "Studying>Instagram";
    break;
  case "discord.com":
    document.body.innerHTML = "Studying>Discord";
    break;
}
